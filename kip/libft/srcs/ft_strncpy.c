/*
** Replicates strncpy (string.h).
**
** Copies up to n characters of the the string src to dst (including the
** terminating `\0' character.)
**
** Returns the pointer to dst.
*/

#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (src[i] && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
