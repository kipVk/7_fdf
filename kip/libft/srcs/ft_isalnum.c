/*
** Replicates isalnum (ctype.h).
**
** Tests if a character is alphanumeric.
**
** Returns 1 if true and 0 if false.
*/

#include "libft.h"

int		ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	else
		return (0);
}
