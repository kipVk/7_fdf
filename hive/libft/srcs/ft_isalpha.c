/*
** Replicates isalpha (ctype.h).
**
** Tests if a character is alphabetic.
**
** Returns 1 if true and 0 if false.
*/

#include "libft.h"

int		ft_isalpha(int c)
{
	if (ft_isupper(c) || ft_islower(c))
		return (1);
	else
		return (0);
}
