/*
** Replicates memcmp (string.h).
**
** Compares byte string s1 against byte string s2. Both strings are assumed
** to be n bytes long.
**
** Returns 0 if the strings are identical; otherwise, returns the difference
** between the first two differing bytes (treated as unsigned char values).
*/

#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	if (s1 == s2)
		return (0);
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}
