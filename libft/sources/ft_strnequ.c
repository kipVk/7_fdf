/*
** Compares (lexicographically) the null-terminated strings s1 and s2, up to n
** characters or the end of the string, whichever comes first.
**
** Returns 1 if the strings are identical; otherwise, returns 0.
*/

#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	else if (ft_strncmp(s1, s2, n) == 0)
		return (1);
	else
		return (0);
}
