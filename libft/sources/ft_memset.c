/*
** Replicates memset (string.h).
**
** Writes len bytes of value c (converted to an unsigned char) to the string b.
**
** Returns the pointer to b.
*/

#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)b;
	while (i < len)
	{
		*ptr = (unsigned char)c;
		ptr++;
		i++;
	}
	return (b);
}
