/*
** Frees the memory of a link and every successors using the functions del
** and free(3). Finally, it sets the pointer to the link that was just freed
** to NULL.
*/

#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*next;

	while (*alst)
	{
		next = (*alst)->next;
		ft_lstdelone(alst, del);
		*alst = next;
	}
	*alst = NULL;
}
