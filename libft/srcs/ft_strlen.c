/*
** Replicates strlen (string.h).
**
** Computes the length of the string s.
**
** Returns the length of the string.
*/

#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
