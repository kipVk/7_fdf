/*
** Replicates toupper (ctype.h).
**
** Converts a lowercase letter to the corresponding uppercase letter.
**
** Returns an uppercase letter.
*/

#include "libft.h"

int		ft_toupper(int c)
{
	if (ft_islower(c))
		return (c - 32);
	else
		return (c);
}
