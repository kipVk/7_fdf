/*
** Frees the memory of a link’s content using the function del,
** then frees the link’s memory using free(3). The memory of next must not be
** freed under any circumstance. Finally, it sets the pointer to the link that
** was just freed to NULL.
*/

#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (!alst || !del)
		return ;
	del((*alst)->content, (*alst)->content_size);
	free(*alst);
	*alst = NULL;
}
