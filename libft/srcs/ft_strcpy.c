/*
** Replicates strcpy (string.h).
**
** Copies the string src to dst (including the terminating `\0' character.)
**
** Returns the pointer to dst.
*/

#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
